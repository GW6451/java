package controlstatement03;

public class KimYouKyung0814 {

	public static void main(String[] args) {
		/*
		 과제]
		 * * * * *  5,1 5,2 5,3 5,4 5,5
		 * * * * 	4,1 4,2 4,3 4,4
		 * * * 		3,1 3,2 3,3
		 * * 		2,1 2,2
		 *          1,1
		           
		 */
		System.out.println("<for문>");
		for(int i=5;i>=1;i--) {
			for(int j=1;j<=5;j++) {
				if(i>=j)System.out.printf("%-2c",'*');
			}
			System.out.println();
		}
		
		System.out.println("<while문>");
		int i=5;
		while(i>=1) {
			int j=1;
			while(j<=5) {
				if(i>=j) System.out.printf("%-2c",'*');
				j++;
			}
			i--;
			System.out.println();
		}
		

	}/////main

}/////class
